# altura e largura

altura = float(input("Digite o valor da altura: "))
largura = float(input("Digite o valor da largura: "))
area = altura*largura
print ("A Dimensão da parede é {} x {}, sendo assim a área é {} ".format(altura,largura,area))

tinta = area/2

print ("A quantidade de tinta necessária é {}".format(tinta))

