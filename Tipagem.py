algo = input("Digite algo: ")

# Verifica se só tem espaços
print("Só tem espaço?", algo.isspace())

# Verifica se é alfabética
print("É alfabética?", algo.isalpha())

# Verifica se é alfanumérica
print("É alfanumérica?", algo.isalnum())

# Verifica se está em maiúsculas
print("Está em maiúsculo?", algo.isupper())

# Verifica se está em minúsculas
print("Está em minúsculo?", algo.islower())
# Verifica se esta capitalizada
print ("Está capitalizada? ", algo.istitle())
